		System.out.println("Time of an event:" + hours + ":" + minutes + " " + meridiem);
		
		//Person's eye color: "Brown"
		String eyeColour = "Blue";	
		System.out.println("Person's eye color: " + eyeColour);
		
		//Person's favourite colour: "Blue"
		String colour = "Blue";	
		System.out.println("Person's favourite colour:" + colour);
		
